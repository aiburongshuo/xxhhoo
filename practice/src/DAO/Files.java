package DAO;

public class Files {

	
	private long fid;
	private String fname;
	private String fmaoshu;
	public long getFid() {
		return fid;
	}
	public void setFid(long fid) {
		this.fid = fid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFmaoshu() {
		return fmaoshu;
	}
	public void setFmaoshu(String fmaoshu) {
		this.fmaoshu = fmaoshu;
	}
	
	
}
